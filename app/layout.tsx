import type { Metadata } from "next";
import { JetBrains_Mono, Orbitron, Barlow, IBM_Plex_Sans } from "next/font/google";
import localFont from "next/font/local";
import "./globals.css";
import { UiStyleProvider } from "./UiStyleProvider";
import { SoundProvider } from "./hud/SoundProvider";
import { ScrollXReset } from "./ScrollXReset";
import { WalletProvider } from "./contexts/WalletContext";

const barlow = Barlow({
  variable: "--font-barlow",
  subsets: ["latin"],
  display: "swap",
  weight: ["400", "500", "600", "700"],
});
const adamHeader = localFont({
  src: "./fonts/adam-header/AdamHeader.woff2",
  variable: "--font-adam-header",
  display: "swap",
});

const jetbrainsMono = JetBrains_Mono({
  variable: "--font-jetbrains-mono",
  subsets: ["latin"],
  display: "swap",
  weight: ["400", "500", "600", "700"],
});

const orbitron = Orbitron({
  variable: "--font-orbitron",
  subsets: ["latin"],
  display: "swap",
  weight: ["400", "600", "700"],
});

const ibmPlexSans = IBM_Plex_Sans({
  variable: "--font-ibm-plex-sans",
  subsets: ["latin"],
  display: "swap",
  weight: ["400", "500", "600", "700"],
});


export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

const uiStyleInitScript = `
  (function () {
    try {
      var saved = window.localStorage.getItem('adam-ui-style');
      if (saved === 'classic' || saved === 'hud') {
        document.documentElement.dataset.ui = saved;
      }
    } catch (e) {
      // ignore
    }
  })();
`;

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" suppressHydrationWarning>
      <head>
        <script dangerouslySetInnerHTML={{ __html: uiStyleInitScript }} />
      </head>
      <body
        suppressHydrationWarning
        className={`${barlow.variable} ${jetbrainsMono.variable} ${orbitron.variable} ${adamHeader.variable} ${ibmPlexSans.variable} antialiased`}
      >
        <WalletProvider>
          <UiStyleProvider>
            <SoundProvider>
              <ScrollXReset />
              {children}
            </SoundProvider>
          </UiStyleProvider>
        </WalletProvider>
      </body>
    </html>
  );
}
